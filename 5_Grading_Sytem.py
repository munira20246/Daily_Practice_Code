n = float(input("Enter Your Marks: "))

if n >= 80 and n <= 100:
    print("\nYour grade is 'A+'!\nYou've got highest grade.\n")

if n >= 75 and n <= 79:
    print("\nYour grade is 'A'!\nYou've Passed!\n")

if n >= 70 and n <= 74:
    print("\nYour grade is 'A-'\nYou've Passed!\n")

if n >= 65 and n <= 69:
    print("\nYour grade is 'B+'\nYou've Passed!\n")

if n >= 60 and n <= 64:
    print("\nYour grade is 'B'\nYou've Passed!\n")

if n >= 55 and n <= 59:
    print("\nYour grade is 'B-'\nYou've Passed!\n")

if n >= 50 and n <= 54:
    print("\nYour grade is 'C+'\nYou've Passed!\n")

if n >= 45 and n <= 49:
    print("\nYour grade is 'C'\nYou've Passed!\n")

if n >= 40 and n <= 44:
    print("\nYour grade is 'D'\nYou've Passed!\n") 

if n >= 0 and n <= 39:
    print("\nYour grade is 'F'\nYou've Failed!\n")           


